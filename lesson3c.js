// Javascript functions
// A function is a block of code that performs a cetain action/task.
// The simple structure of a function is given below:
// function keyword, name of the function, parenthesise the body of the function inside of curly braces.
function sayHello(){
    console.log("Hello there, Hope you are having a good day.")
}
// Call/invoke the function by use of the name of the function.
sayHello()

